#!/usr/bin/env python3

import pytest
from bio96 import *

def test_iter_ij_in_block():
    examples = [
            (('A1', 0, 0), []),
            (('A1', 0, 1), []),
            (('A1', 1, 0), []),
            (('A1', 1, 1), [(0,0)]),
            (('A1', 2, 1), [(0,0), (0,1)]),
            (('A1', 1, 2), [(0,0), (1,0)]),
            (('A1', 2, 2), [(0,0), (0,1), (1,0), (1,1)]),

            (('A2', 0, 0), []),
            (('A2', 0, 1), []),
            (('A2', 1, 0), []),
            (('A2', 1, 1), [(0,1)]),
            (('A2', 2, 1), [(0,1), (0,2)]),
            (('A2', 1, 2), [(0,1), (1,1)]),
            (('A2', 2, 2), [(0,1), (0,2), (1,1), (1,2)]),

            (('B1', 0, 0), []),
            (('B1', 0, 1), []),
            (('B1', 1, 0), []),
            (('B1', 1, 1), [(1,0)]),
            (('B1', 2, 1), [(1,0), (1,1)]),
            (('B1', 1, 2), [(1,0), (2,0)]),
            (('B1', 2, 2), [(1,0), (1,1), (2,0), (2,1)]),
    ]
    for args, expected in examples:
        print(args)
        assert set(iter_ij_in_block(*args)) == set(expected)
